; Задача: hello_user_cstr
;
; Что делает программа:
; 1) Выводит вопрос "What is your name?\n" в io[0x84].
; 2) Читает ввод из io[0x80] до '\n', максимум 23 символа.
; 3) Если имя пустое ИЛИ ввод усечен по лимиту, выводит overflow value 0xCCCCCCCC.
; 4) Иначе формирует C-строку в буфере 0x00..0x1F:
;      "Hello, " + takewhile(c != '\0') + "!\0"
;    и печатает ее в io[0x84] без завершающего '\0'.
;
; Дополнительно:
; - Внутренний '\0' в имени обрезает имя (как в takewhile).
; - Буфер результата заполняется в greet_buf (32     байта).
; - Есть вложенный вызов процедур: _start -> write_greeting -> write_str.

    .data

; Буфер greeting, ровно 32 байта (0x00..0x1F), стартовое заполнение '_'
greet_buf:    .byte  '________________________________'

; Константный вопрос
str_question: .byte  'What is your name?\n\0' ; 20 байт
str_hello: .byte  'Hello, \0' ; 7 байт  

    .text
    .org 0x88

; ════════════════════════════════════════════════════════════════
; _start — точка входа.
; a0/a1 используются для аргументов и возврата,
; ra сохраняется в стеке перед вызовами процедур.
; ════════════════════════════════════════════════════════════════
_start:
    lui  sp, %hi(0xF00)
    addi sp, sp, %lo(0xF00)                 ; вершина стека (инициализация)

    ; --- 1. Вывести "What is your name?\n" ---
    lui  a0, %hi(str_question)
    addi a0, a0, %lo(str_question)          ; тут мы загрузили в a0 адрес где лежит str_question
    
    addi sp, sp, -4                         ; освободили место для (1 слова = 4 байта) в стеке
    sw   ra, 0(sp)                          ; тут мы сохранили return address (ra) c стек, чтобы было куда возвращаться, так как при прыжке в write_str внутри этой функции может быть еще одна которая затрет текущий ra  
    jal  ra, write_str                      ; Прыгаем в write_str, сохраняя адрес возврата в ra
    lw   ra, 0(sp)                          ; после write_str востанавливаем ra из стека
    addi sp, sp, 4                          ; освобождаем стек

    ; --- 2. Читать имя сразу в greet_buf+7 (после префикса "Hello, ") ---
    ; read_line возвращает:
    ;   a0 = длина прочитанного ввода до '\n'
    ;   a1 = 1 при усечении по max_len, иначе 0
    lui  a0, %hi(greet_buf)
    addi a0, a0, %lo(greet_buf)             ; сохранили в стек адрес где будет лежать имя
    addi a0, a0, 7                          ; a0 = адресс greet_buf + 7 байт (7 байт = "Hello, ")
    addi a1, zero, 23                       ; инициализируем a1 ровно 23 байтами  

    addi sp, sp, -4                         ; расширяем стек на 1 слово
    sw   ra, 0(sp)                          ; сохраняем ra в стек
    jal  ra, read_line                      ; вызвываем функцию чтения - _start → read_line; a0 = длина
    lw   ra, 0(sp)                          ; востанавливаем ra из стека
    addi sp, sp, 4                          ; очищаем стек

    ; --- 3. Если имя пустое ИЛИ усечено по лимиту — вывести overflow ---
    beqz a0, start_overflow                 ; Если a0 == 0 (имя пустое), прыгнуть в ошибку
    bnez a1, start_overflow                 ; Если a1 != 0 (произошло усечение, просто флаг на переполненение), прыгнуть в ошибку

    ; --- 4. Иначе собрать greeting в буфере и вывести ---
    addi sp, sp, -4                         ; расширяем стек на 1 слово
    sw   ra, 0(sp)                          ; сохраняем ra в стек
    jal  ra, write_greeting                 ; a0=len, _start → write_greeting → write_str (Вызов сборки строки (a0 сейчас хранит длину имени))
    lw   ra, 0(sp)                          ; востанавливаем ra из стека
    addi sp, sp, 4                          ; очищаем стек
    j    program_end

start_overflow:
    ; Overflow-ветка: одно слово 0xCCCCCCCC в io[0x84]
    lui  t1, %hi(0xCCCCCCCC)
    addi t1, t1, %lo(0xCCCCCCCC)
    addi t0, zero, 0x84                     ; тут мы в t0 загрузили адресс вывода 
    sw   t1, 0(t0)                          ; сохраняем результат
    
    j    program_end    

program_end:
    halt

; ════════════════════════════════════════════════════════════════
; write_greeting(a0 = длина ввода до '\n')
; Формирует C-строку в greet_buf:
;   "Hello, " + имя + "!\0"
; При этом имя обрезается на первом '\0' внутри входа.
; Затем печатает строку через write_str (без вывода '\0').
; ════════════════════════════════════════════════════════════════
write_greeting:
    addi sp, sp, -8                          ; увеличиваем стек на 8 байт = 2 значения
    sw   ra, 4(sp)                           ; Сохраняем адрес возврата (ra), так как внутри мы будем вызывать write_str.
    sw   a0, 0(sp)                           ; Сохраняем длину имени (она пришла в a0), так как a0 нам понадобится для других целей.

    lui  t0, %hi(greet_buf)                  ;
    addi t0, t0, %lo(greet_buf)              ; t0 = адрес начала буфера, куда мы собираем строку (destination).

    lui  t1, %hi(str_hello)
    addi t1, t1, %lo(str_hello)              ; t1 = адрес начала строки "Hello, " (source).

    addi t2, zero, 0xFF                      ; Маска

copy_hello_loop:
    lw   t3, 0(t1)                          ; Читаем 4 байта из str_hello (но работаем только с младшим).
    and  t3, t3, t2                         ; t3 <- t3 & 0xFF (Очищаем верхние 24 бита, оставляя только текущий символ.)
    beqz t3, copy_hello_done                ; Если символ равен 0 (конец строки "Hello, "), выходим из цикла.
    sb   t3, 0(t0)                          ; Записываем этот символ в начало greet_buf.
    addi t0, t0, 1                          ; Сдвигаем указатель записи (dest) на 1 байт вперед.
    addi t1, t1, 1                          ; Сдвигаем указатель чтения (src) на 1 байт вперед.
    j    copy_hello_loop                    ; Переходим к следующему символу.

copy_hello_done:
    ; t0 теперь указывает на начало имени (greet_buf + 7)
    lw   t4, 0(sp)                          ; t4 = длина имени (Достаем из стека длину имени, которую сохранили в самом начале).
    addi t5, zero, 0                        ; t5 — это наш счетчик цикла (i = 0).

find_name_end:
    beq  t5, t4, name_end_found             ; Если i == длина имени, значит мы прошли всё имя.
    lw   t3, 0(t0)                          ; Читаем символ имени из буфера.
    and  t3, t3, t2                         ; Маскируем байт.
    beqz t3, name_end_found                 ; Если встретили '\0' внутри имени, останавливаемся здесь (усечение).
    addi t0, t0, 1                          ; Сдвигаем указатель вперед.
    addi t5, t5, 1                          ; i++
    j    find_name_end                      ; Повторяем для следующего символа.

name_end_found:
    addi t3, zero, 33                       ; t3 <- zero + '!'
    sb   t3, 0(t0)                          ; mem[t0 + 0] <- t3 = '!'
    sb   zero, 1(t0)                        ; mem[t0 + 1] <- zero

    ; Вывод результата
    lui  a0, %hi(greet_buf)
    addi a0, a0, %lo(greet_buf)             ; a0 = адрес начала итоговой фразы. 
    jal  ra, write_str                      ; Вызываем процедуру печати.

    lw   ra, 4(sp)                          ; Достаем из стека адрес возврата в _start.
    addi sp, sp, 8                          ; Возвращаем указатель стека на место.
    jr   ra                                 ; Прыгаем назад в _start.

