read_line_done:
    addi a1, zero, 0                         ; a1 <- zero + 0 (не усечено)
    j read_line_common                       ; прыгнуть к общему коду

read_line_truncated:
    addi a1, zero, 1                         ; a1 <- zero + 1 (усечено по лимиту)
    j    read_line_common

