; ════════════════════════════════════════════════════════════════
; write_str(a0 = ptr на C-строку)
; Выводит байты в io[0x84] до первого '\0' (сам '\0' не выводится).
; Листовая процедура.
; ════════════════════════════════════════════════════════════════
write_str:
    addi t0, zero, 0x84                      ; Адрес вывода
    addi t2, zero, 0xFF                      ; маска для выделения байта

write_str_loop:
    lw   t1, 0(a0)                           ; t1 <- mem[a0 + 0] (читаем слово, используем младший байт)
    and  t1, t1, t2                          ; t1 <- t1 & 0xFF (оставляем только младший байт)
    beqz t1, write_str_done                  ; Проверка на конец строки ('\0' — конец строки)
    sb   t1, 0(t0)                           ; mem[t0 + 0] <- (t1 & 0xFF)(пишем байт в 0x84)
    addi a0, a0, 1                           ; a0 <- a0 + 1 (следующий символ) 
    j    write_str_loop                      ; просто вернуться в начало цикла

write_str_done:
    jr   ra                                  ; pc <- ra

; ════════════════════════════════════════════════════════════════
; read_line(a0 = dst, a1 = max_len)
; Читает из io[0x80] до '\n' или max_len, затем пишет '\0' в dst.
; Возврат:
;   a0 = число записанных символов,
;   a1 = 1 если достигнут max_len до '\n' (усечение), иначе 0.
; Листовая процедура.
; ════════════════════════════════════════════════════════════════
read_line:
    addi t0, zero, 0x80                      ; t0 <- zero + 0x80 (адрес ввода)
    addi t2, zero, 0                         ; t1 <- zero + 0 (счётчик символов)
    addi t3, zero, 0xFF                      ; t3 <- zero + 0xFF (маска байта)
    addi t4, zero, 10                        ; t4 <- zero + ('\n' = 10)

read_line_loop:
    beq  t2, a1, read_line_truncated         ; if (t2 == (a1 = 23)) лимит достигнут раньше '\n' ==> read_line_truncated
    lw   t1, 0(t0)                           ; t1 <- mem[t0 + 0] (читаем символ из 0x80)
    and  t1, t1, t3                          ; t1 <- t1 & 0xFF (маска байта)
    beq  t1, t4, read_line_done              ; if (t1 == (t4 = '\n')) '\n' завершает чтение ==> read_line_done
    sb   t1, 0(a0)                           ; mem[a0 + 0] <- t1 (пишем символ в буфер)
    addi a0, a0, 1                           ; a0 <- a0 + 1 (следующий байт буфера)
    addi t2, t2, 1                           ; t2 <- t2 + 1 (счётчик++)
    j    read_line_loop                      ; возвращаемся

