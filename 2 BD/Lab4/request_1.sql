EXPLAIN ANALYSE SELECT "Н_ЛЮДИ"."ИМЯ", "Н_СЕССИЯ"."УЧГОД"
FROM "Н_СЕССИЯ"
RIGHT JOIN "Н_ЛЮДИ" ON "Н_СЕССИЯ"."ЧЛВК_ИД" = "Н_ЛЮДИ"."ИД"
WHERE "Н_ЛЮДИ"."ОТЧЕСТВО" < 'Георгиевич'
  AND "Н_СЕССИЯ"."ЧЛВК_ИД" < 151200
  AND "Н_СЕССИЯ"."ЧЛВК_ИД" < 100622;